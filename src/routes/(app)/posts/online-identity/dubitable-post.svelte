    <p>
        I was recently collecting interesting RSS feeds to read and came across <a
            href="https://brennan.day/a-ship-in-harbour-is-safe/"
            >this post by brennan.day</a
        >. I'd had this idea for a while but it was a useful way to frame why I
        use this moniker and thoughts on identity online in general. We can
        cover the post and my history of aliases.
    </p>
    <p>
        The basic gist of Brennan's post is that using your real name on the
        internet means there's higher stakes for things. You should invest in
        things you care about to enrich them with value and meaning. This also
        includes having the implications of people finding your posts and
        judging you based on them. You should be able to stand behind the words
        you write and justify them which is a nice sentiment.
    </p>
    <Quote by="brennan.day">
        We've also become a culture of taking the safest path forward... Safety
        is so much smaller than risk.
    </Quote>
    <p>
        I don't disagree with this, but I think he is too dismissive of an
        online identity as not meaningful in its own way and there are plenty of
        reasons people have for not associating an online identity with their
        real life one. There are many great political reasons for this which I
        won't go into but I'm sure you can imagine all the exquisite writing I
        could do on that topic.
    </p>
    <p>
        When I was young I had no idea what username to use. For online games <Footnote
            >This mostly meant games like Poptropica or other flash games but
            also things like my Chess.com account.</Footnote
        > I typically just used my first name with some numbers after. However I didn't
        feel comfortable with this; I am naturally reticent to share any information
        about myself on the internet. It also felt like tethering myself too much
        to these accounts since I didn't have a strong idea of what I wanted to be
        on the internet. I tried a bunch of other usernames, which is how my Steam,
        Battle.Net and Reddit accounts all have different usernames. I don't mind
        this actually, there's no real connection between them so it's good for them
        not to be linked. <Footnote
            >No I won't tell you what they are sorry.</Footnote
        > Eventually I decided that I just wanted a username on Twitch that was a
        word in the dictionary. My steam name at the time was 'Indubitably' because
        I was slightly pretentious<Footnote>I still am probably.</Footnote> so I tried
        a few variations on that word until I found that dubitable wasn't taken so
        I used that and eventually it became my default username to use.
    </p>
    <p>
        I've used it for near on 10 years now, made a lot of mistakes under the
        name and met plenty of cool people<Footnote
            >Digitally, not physically met.</Footnote
        >. It would be difficult for me to just restart with a new alias. Yes I
        am anonymous but in some ways 'dubitable' is my main identity online. I
        don't use my real name on any websites that I visit regularly so it's
        almost like a parallel world for me. I do understand why people would
        want to use their real names online, there's professional benefits in it
        since this is a kind of marketing for yourself on the labour market.
        There's also advantages in being able to associate with real life
        conferences/events and people which may not come easily using an
        anonymous identity. I like to ask people online how they came up with
        their username, mostly because I'm curious about these things and often
        there's a funny story underling that choice. It can range from just
        someone's name to an elaborate backstory or perhaps they saw the name in
        a video game or movie and liked it.
    </p>
    <p>
        It used to be more ingrained that people would use an anonymous identity
        online, especially gamers. I think this was just a natural repulsion to
        associate the world of the internet with real life. You had an
        opportunity to find new acquaintances and not worry about them caring
        about your real life past for whatever reasons they might be. Especially
        in things like IRC chats it was natural to pick an alias that wasn't
        associated with anything.
    </p>
