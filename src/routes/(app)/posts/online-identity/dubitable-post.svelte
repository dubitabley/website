    <p>
        Obviously there are many negative consequences of anonymity. Most
        notable is that if it's easy to create anonymous accounts then there can
        be no consequences for bad behaviour so people feel emboldened into
        harmful actions such as trolling or being hateful. This is certainly a
        problem but I don't believe the solution lies in removing anonymity but
        in creating systems that allow new users in different ways, such that
        they always favour good actors so that people who are harmful can't
        overwhelm things. This is obviously a tricky balance since you don't
        want to close things off so new users can't join but necessary to avoid
        moderation becoming an overwhelming task. Having a financial cost to
        joining is an option but causes many problems of its own. This topic
        deserves its own post but I'll just say that <i>anonymity is worth it</i
        >.
    </p>
    <p>
        Identity online is strongly tied to the communities you're a part of.
        Some of these may be also tied to your real life identity and community.
        This includes things like religions, sports you play, places you live in
        etc. I undrstand for these groups it's more natural to use an identity
        tied to your real life identity since that's an important connection but
        for things that are more abstract, where connections and communities can
        form across the world without any other links, such as gaming,
        identities feel more natural as split out from your real one. You can
        isolate mistakes and social errors which is important since it's only
        human to do these things. I've said and done things I regret <Footnote
            >Nothing that's <i>too</i> bad I think.</Footnote
        > and it's nice to know that it's somewhat isolated.
    </p>
    <p>
        Finally, identity is important. Online it mostly consists of a username,
        a profile picture and perhaps a short biography if you're up to it. I
        think it's worth thinking about what kind of identity you want to have,
        both offline and online. How will others perceive you? From that, how do
        you judge others based on their usernames or profile picture? It's often
        valuable to think about these things actively rather than passively or
        implicitly so you can change if you want.
    </p>
