    <h3>History</h3>
    <p>
        Bat and ball sports have existed in many different places and times but
        the modern sport originates from England and was spread by the British
        Empire across the world; today it's mostly played in England/Wales,
        Australia, New Zealand, India, Pakistan, Sri Lanka, South Africa,
        Bangladesh and the West Indies (collection of countries around the
        Caribbean) although it has spread to an assortment of various other
        nations.
    </p>
    <p>
        Its nature as originating from England does lend itself to some
        anachronistic practices. It's played by the historic counties of England
        which have funny names like 'Nottinghamshire', 'Gloucestershire' and
        'Worcestershire'.<Footnote>Famed from worcestershire sauce</Footnote> Over
        their history the primary form of cricket they've played, called First Class
        cricket is played over multiple days which meant that the only people who
        were able to play were professionals who could muster a small wage from their
        playing or amateurs who were more wealthy. This resulted in a strange tradition
        where a match was held for professionals against amateurs although in typical
        fashion it was named 'Gentlemen v Players'. Like many traditions this one
        also fell by the wayside, the concept of amateur players as formalised was
        abolished and so was the match in 1963. No tradition is forever.
    </p>

