    <h3>Fast cricket</h3>
    <p>
        Cricket over multiple days is fantastic since like all entertainment
        it's about telling stories. The longer the game the more grand the story
        is, the more it can ebb and flow. However it is a long time to get
        someone to invest to watch a game so there were experiments with
        designing a shorter form of the sport. In the 1970s a format called One
        Day International cricket started to be played which meant a game could
        be held over a single day over around 8 hours. It also meant that it
        could be played at night which is a better time commercially since
        people could watch it after work. There could be other developments
        focused on these kinds of flashy things such as teams wearing coloured
        clothing instead of the traditional white.
    </p>
    <p>
        However even this was too long for most people. Football matches are 90
        minutes on the field plus a bit for breaks/added time. Rugby is 80
        minutes. Basketball is around 40-48 minutes. Note that all these sports
        take a bit longer than these times because of breaks but nothing close
        to the whole day affair of a one day match. Thus an even shorter version
        of the game was developed and trialled in England in the early 2000s
        called Twenty20 or T20 for short. This lasted only 3-4 hours and was a
        breakneck pace compared to the other formats.
    </p>
    <p>
        This new format was exactly what was needed for it to explode in
        popularity, especially in the Indian sub-continent. The IPL or Indian
        Premier League has a staggeringly high amount of money running through
        it. Other T20 leagues are also the dominant form of cricket in the
        cricket playing nations and the format essentially keeps the finances
        alive for the cricket administrations. The top players can get millions
        of dollars for just a few months of playing. Gone were the days of
        having amateurs playing against professionals; just a distant memory at
        this point.
    </p>
    <h3>Spirit of Cricket</h3>
    <Quote by="George Orwell">
        Serious sport has nothing to do with fair play. It is bound up with
        hatred, jealousy, boastfulness, disregard for all rules and sadistic
        pleasure in witnessing violence: in other words it is war minus the
        shooting
    </Quote>
    <p>
        Sportsmanship is sometimes a nebulous topic, people may not always agree
        on what is and isn't good sportsmanship. They tend to be things that
        can't be easily described or written down and objectively ruled on. It's
        more a case of 'I know it when I see it'.<Footnote
            >A quote from the US Supreme Court Justice Potter Stewart. It may
            sound like a cop-out and a deriliction of the responsibility to make
            a proper ruling on things but it does have the advantage of
            reflecting the fact that mores in society tend to change over time.
        </Footnote>
        In the preamble to the Laws of Cricket, before any laws are stated is the
        Spirit of Cricket, about respect and making the game a more cordial and enjoyable
        atmosphere for all. In the English language, "it's just not cricket" is a
        common phrase indicating something that's unfair or dishonest, indicating
        how much cricket is associated with this spirit of gentlemanly conduct.
    </p>
    <p>
        Of course there are hypocrises to this. Whether it's Bodyline,
        Sandpapergate, Underarm, sledging or various smaller events there have
        always been instances of players disrespecting what is considered the
        'right' thing to do. However I still believe it's a useful fiction to
        maintain in that if people believe it's right then in general it leads
        to better conduct through social pressure. With the rise of enormous
        amounts of money in the game it makes it all the more tempting to
        stretch the rules to their absolute limit; to employ deception and
        conniving that makes the game more hostile and worse for everyone.
    </p>

