<h1 class="utopia-text">
    Utopia.
</h1>
<p>
    I love this word. <br />
    It promises so much. However the world seems so far from it in so many ways. This book 
    is a utopian novel; perhaps <strong>the</strong> utopian novel since it is the 
    origin of the word. Written a long time ago by Thomas More it lays out a dialogue of 
    someone who has visited an island a distance quite far away and describes its customs
    and how it re-imagines society.
</p>
<p>
    Thomas More is himself an interesting person as the following roles/names: 
</p>
<ul>
    <li>Undersheriff of London</li>
    <li>Sir</li>
    <li>Speaker of the House of Commons</li>
    <li>Lord Chancellor</li>
    <li>Traitor</li>
    <li>Saint</li>
</ul>
<p>
    He was born in 1478, knighted in 1521, made Lord Chancellor in 1529, was executed as 
    a traitor in 1535 and made a saint in 1935. What's interesting about this time, especially 
    in relation to the text is that the "New World" was just now being "discovered" and 
    discussed in Europe.
    There is a play and movie about him called "A man for all seasons" which is fairly 
    reputable and is an interesting story of the events leading up to More's death. 
</p>
<p>
    An old and famous novel like this is sure to have many interpretations and essays
    and in fact the copy I read had both an introduction and an afterword along 
    with footnotes scattered throughout the text. Since it was written in Latin it's 
    sometimes useful to have a note on the way it's been translated but having 
    "Sidenote: <i>How true and well put</i>" and "Sidenote: <i>Note this, reader</i>"
    doesn't tend to add much to my reading of it.
    Therefore, I will try to forgo the pithy re-interpretations that I read during the 
    peripheral notes and speak more about what utopia should mean to me.
</p>
<p>
    I've read a few other Utopian novels previously, such as 
    <a href="https://en.wikipedia.org/wiki/News_from_Nowhere">News from Nowhere</a> by William Morris
    and <a href="https://en.wikipedia.org/wiki/Looking_Backward">Looking Backward</a> by Edward Bellamy.
    The most notable thing about these books is that they're old. We no longer believe in utopia; 
    it's more like nowhere than it was for More.
    The only things that we as a society can imagine are dystopias, worlds which are 
    engineered in a similar fashion to the Utopia that was designed by Utopus but 
    are bad in various ways. Indeed while Utopia literally is u-topia or no place; 
    it could also be read as eu-topia or good place while dystopia merely means bad place.
</p>
<p>
    This has been a bit of a depressing essay, very light on details from the text but 
    the book itself is not super interesting; sometimes it goes into excruciating 
    detail about some aspects of the society. Obviously there is the caveat that the 
    whole description of Utopia is through a narrator in the form of Raphael Hythloday  
    who may be interpreting things in his own way. The most interesting parts of Utopia 
    the society to me are how static and uniform things seem to be; reminding me of 
    a dystopian world. The island was setup in 244 B.C. by Utopus (apparently a reference 
    to Agis IV of Sparta) and one of its qualities is its stability and 
    lack of social and political upheaval. However this also seems to inhibit 
    creativity and for people to live in unique ways. Of course the world we live in 
    also can feel stifling and this is true of many criticisms of the society as described.
    It is merely a way to imagine a world built on different foundations with other 
    ways of thinking about what is a just and moral life.
</p>
<p>
    Another fascinating point is the kinds of activities that appear to be of elevated
    importance to the Utopians. Seemingly they are fascinated with learning and craft
    and "nice" artisanal. Every household has their own garden and they enjoy cultivating a healthy and 
    prosperous garden as a miny kind of competition. I find the fact that they want to learn and study as an end
    in and of itself rather than for some purpose. They're not omnipotent and 
    still have things to learn as well as the European travellers bring them paper 
    and they attempt to reverse engineer how it's created so that they can use it themselves.
    Perhaps if the book was written after the industrial revolution it would 
    emphasise more science, technology and transport. Perhaps Utopia today
    would emphasize computing, information and an international or even inter-galactic outlook.
    Even though Utopia was meant to be a way of imagining a society completely different 
    from the one Thomas More resided in, it was still a construct of his own time and life.
    What would Utopia today be like? Who would it be for? What example could it show?
</p>
<p>
    I don't have a way to finish this that feels adequate other than this.
</p>
<h3 class="utopia-text">
    Keep dreaming of Utopia.
