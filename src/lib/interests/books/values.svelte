<Post>
    <p>
        This is a book written by Mark Carney after leaving as Governor of the
        Bank of England but a few years before prime minister. What I want to do
        here is to do a review of the book and then try to look through as many
        reviews and discussion of the book online as I can find and see what
        other people think of it. What will be interesting is to see the
        discussion before and after Carney became prime minister and how that
        context changes the way people view the book.
    </p>

    <p>
        The start is very dull. Perhaps I am expecting too much of a central
        banker but there is absolutely no personality that shines through. Even
        as the text speaks in first person it absolutely refuses to actually
        make clear that it's expressing an opinion; instead preferring to use a
        textbook style of imparting information. The first section of the book
        appears mostly as a kind of basic economics history lesson but doesn't
        feature much in the way of analysis, just plenty of narrativising and
        covering what might be taught over an economics course at university.
        This section is alright and somewhat interesting although it seems a bit
        meandering and directionless without being directly connected as much to
        the later sections. I do understand why it's here though.
    </p>

    <p>
        In the section on the Global Financial Crisis of 2008<Footnote
            >It's hard to keep track of which one we're talking about sometimes.</Footnote
        > he tends to discuss specific policy changes that were made in response to
        it and explain how they could prevent the issues that caused the problems.
        However he doesn't really link these back to the fundamental values the book
        professes to be about and never explains how these kinds of resiliency changes
        can be baked into decision making in the future. They are fundamentally just
        technocratic changes rather than systemic ones and could easily be rolled
        back as the lessons learned fade. It's bizarre how he acknowledges the boom
        and bust cycle has been inevitable for centuries and knows that "this time
        is different" is a phrase that has never been true but he seems to think that
        the particular changes they instituted as part of the Financial Stability
        Board<Footnote
            >The FSB, not to be confused with the security services of Russia.</Footnote
        > will last and can stave off financial crashes. Though those restrictions
        may have temporarily left the financial system more resilient, the crisis
        as a whole left society more fragile. This obviously isn't just an issue in
        finance; it is sometimes said darkly that regulations are written in blood
        since humans are terrible at placing enough value in risk management whether
        natural or human caused. This leads on to the next section, about COVID-19
        and its consequences and fallout.
    </p>

    <p>
        The COVID-19 section is another lesson in bizarre optimism despite the
        massive failings. The text can recount how governments were completely
        unprepared for the pandemic, having underfunded public health systems
        for decades and not stocking up on essential equipment required for
        dealing with this foreseeable crisis. There was during the early days of
        the pandemic a common sentiment that we couldn't simply go back to
        normal as before it there was massive inequities and COVID-19 shutting
        things down was an opportunity to reset and look at fixing many of those
        glaring issues in society. However as lockdowns and restrictions dragged
        on, the idea of "Building back better" fell apart and instead society
        picked itself back up into an even more tenuous state. The book was
        published in 2021 and still carries those early optimistic tendencies of
        society bandying together to stave off the virus and saving lives. Even
        then there was signs of the fracturous nature of the consequences of the
        pandemic and the restrictions as the unity could only last so far.
    </p>

    <p>
        After that is a lengthy section on climate change which is much more
        convincing as to how screwed we are than to any postiive scenarios.
        There is an extraordinary number of diagrams and analyses in this
        section than the other sections which makes sense given that Carney's
        role after leaving the bank was focused on addressing this issue using
        the tools of finance. There's not much to analyse here, the section
        covering the problems associated with climate change as well as the
        projections of where we're headed is mostly good although still feels
        marginally rushed because of the nature of being just one chapter in a
        book. i.e. too technical for a brief overview but not technical enough
        for deeper understanding. The section on how it could be addressed
        really just comes off as waffling though, nothing concrete is shown and
        it's mostly around what could be or vague statements or gestures. Carney
        says "Companies take their cue from what society needs and wants."
        without justifying this statement or linking it to anything.
    </p>

    <p>
        If the previous part could be accused of waffling, this next section on
        leadership is much worse. It really feels like a padded out Linkedin
        post with generic listicles to not say much. We get a list of four
        components of transformational leadership, six steps to rebuild public
        trust, three things leaders must do, five "essential and universal
        attributes of leadership". To cap it off we even get three virtues for
        moral leadership. It really comes off as padding to try to use a lot of
        words to not say very much of value. When he threatens to actually have
        an interesting discussion or explain something that might be disputed by
        people, he refuses to investigate. For example, Carney shows a disdain
        for populist politics without ever defining the boundaries of what it is
        and isn't, making it seem as though he just lazily believes in the
        status quo without visiting the core assumptions and reasons behind
        those beliefs.
    </p>

    <p>
        There are plenty of lazy issues in the book that should have been picked
        up by a competent editor. Many of the diagrams in the Climate Change
        section seem to only be clear when displayed in colour but since the
        book is printed in black and white it often isn't clear which line
        matches which in the key. There are chapters which have the same phrase
        repeated multiple times. e.g. in the COVID-19 section he says "We have
        acted as Rawlsians and communitarians not utilitarians or libertarians."
        multiple times; other 'snappy' phrases are similarly overused. He
        misspells Thomas Paine's name as Thomas Payne. It all stinks of a book
        rushed and a lack of care in the final product and covering over the
        cracks of not having a substantive critique for the way society is
        structured other than a paper thin one about the marketisation of
        everything with no substantive solution to this problem.
    </p>

    <p>
        The final chapters are even more bloated. The most common word seems to
        be 'should' as the text appears to be corporate fanfiction as if
        companies will pursue ESG(environmental, social and governance)
        objectives even when they are a detriment to near term profits. There
        are examples of companies that have taken on some of these objectives
        but they never seriously clash with their other commitments and it's
        hard to see how the totality of these actions could even come close to
        reaching the scale of the issues we face. Carney does try to make the
        argument that being an ethical corporation is correlated with higher
        profits but I believe that this is one instance where the causation most
        likely goes the other way. i.e. corporations that are already successful
        think it's valuable to improve their reputation rather than these
        measures leading to the success. I could be wrong though.
    </p>
    <p>
        The second last chapter("How countries can build values for all") does
        include some interesting parts since it's about a philosophy for
        governing a nation which may be relevant for Carney in his current job.
        It's the most direct and well written section of the whole book. He is
        actually quite perceptive about various issues from AI to inflation in
        this section although the solutions are bloviated and would seem to me
        to be tinkering at the edges rather than getting to the core of
        structural problems that have caused the crises and will do so again.
        Plenty of them are wishcasting about how companies should be responsible
        citizens without a clear way that can be achieved. Maybe this is a
        result of Carney not wanting to burn any bridges but he never directly
        criticises anyone in this book except perhaps a minor critique of Milton
        Friedman. He doesn't really tackle contentious subjects or how they may
        contribute to the social malaise. He does mention how globalisation has
        "simultaneously brought great prosperity and caused tremendous misery"
        but again he doesn't really want to tackle the large corporations
        abusing this system and causing the problems in communities, instead
        focusing on technical changes like workforce training and tax
        incentives. It is hard to see how one could possibly think the scale of
        these solutions match the scale of the problem.
    </p>
    <p>
        The conclusion is when Carney actually uses his poetic side to write in
        a more interesting way. It is a little vacuous but the prose is
        appreciated at least. Although he doesn't quite seem to grasp how the
        word 'meme' is used in common parlance, since he uses it in a serious
        context.
    </p>
    <p>
        Now Mark Carney is prime minister of Canada through a series of
        fortunate events for him, one pertinent quote from the book is this one:
        "One of the most important initiatives is carbon pricing. The best
        approach is a revenue-neutral, progressive carbon tax. ... Meaningful
        carbon prices are a cornerstone of any effective climate policy
        framework." <br />
        From
        <a href="https://en.wikipedia.org/wiki/Carbon_pricing_in_Canada"
            >Wikipedia</a
        >: "On March 14, 2025, in his first act as prime minister, Mark Carney
        signed a prime ministerial directive to effectively remove the federal
        consumer carbon tax implemented via the GHGPPA<Footnote
            >Greenhouse Gas Pollution Pricing Act</Footnote
        > by setting it to 0% effective April 1, 2025. However, the industrial carbon
        tax still remains in place." This was blatantly short term politicking ahead
        of strong long-term climate policy and made it more difficult to revisit that
        policy in future.
    </p>
    <p>
        The main purpose of the book seems to be so that Mark Carney has written
        a book which both boosts his credentials as an intelligent thinker but
        also allows him to go on various shows or podcasts discussing it. There
        are some interesting ideas explored but never in depth or ever going
        over opposing views. He appears to recile from conflict or discussion in
        favour of platitudes and cliches. It is competently written but very
        formally and lacking in any kind of style; there are multiple repeated
        phrases and other lazy aphorisms that seem to just be to pad the book.
    </p>

