<Post>
    <p>
        The foundation series are an excellent piece of early science fiction in
        the 1940s and 50s. They were originally a series of short stories but
        later added to with novellas. What I appreciate about this approach is
        that each story feels self-contained while still building on the
        previous ones. Some of them do feel a little rushed since the characters
        can't be fully fleshed out and their motivations often aren't examinged
        which is something that I would've appreciated a more in depth analysis
        of.
    </p>

    <p>
        As a brief synopsis, there is a massive galactic empire which is
        powerful and has been in place for thousands of years, however a man
        called Hari Seldon using a technique called psychohistory predicts that
        it will inevitably collapse and there will be a period of "dark ages"
        unless there is a plan to avoid it. Thus he sets up a Foundation that
        must follow his plan to create a new civilisation which will bloom from
        the ashes of the old empire. However he doesn't give them the plan;
        instead it's a plan that will instead probabilistically be naturally
        followed from his mathematical calculations.
    </p>

    <p>
        One thing that the text tends to treat as a given is that the Foundation
        is good, just and moral which is almost a religious belief which becomes
        a self-fulfilling prophecy for the people involved. Part of the
        complexities of the plan as ordained by Seldon are that the people
        involved have the information about the plan that he's provided and thus
        this must be included as part of the calculations. However psychohistory
        is not really examined in depth other than being a science fiction
        version of prophecy where the protagonists wish for it to come to
        fruition. In this way it uses a sense of order as a stand-in for being a
        sympathetic character and anyone wishing to disrupt that order is seen
        as malevolent. My criticism is that it doesn't have the necessary time
        to justify this framing - we don't see the suffering that might result
        from an alteration to the plan or the happiness from a flourishing
        Foundation.
    </p>

    <p>
        One of my favourite tenets with these kinds of future is the idea that
        the best way to predict the future is to describe the present. Very
        often I will see comments remarking on how it's like some song or story
        or other is predicting the future when in reality it's just talking
        about the world as it is. <Footnote>
            As an example of this, the folk singer Phil Ochs described himself
            as a topical singer and wrote songs about the news of the day,
            however the topics he wrote about are still as relevant as they were
            then. He sung about war, US adventurism, racism and the death of the
            American liberal class.
        </Footnote>
        Since it was written in the 1940s/50s many of the topics reflect strands of
        thought at the time. The idea of a society run by scientists or psychologists
        is somewhat antiquated. The Marxist belief of history moving inexorably and
        inevitably towards a certain state is a critical component of the story. Many
        of the technology mentioned is extremely antiquated such as the power systems
        being coal/gas or nuclear. There's no computing or robotics but the advantage
        of this is that we can focus more on the people involved.
    </p>

    <p>
        The crises the Foundation encounters are pseudo-religious events where
        they affirm the capital T Truth of the plan being followed. A
        significant difference is that the predictions unlike most prophecies
        are more concrete temporally and specific in the events that occur. <Footnote
        >
            For example, Nostradamus is probably the most famous seer but if you
            actually look at his predictions and the events being mooted to fit
            them then they're often very tenous links. The singer Al Stewart
            wrote <a href="https://youtu.be/qa89bt0GZvQ">this song</a> about Nostradamus
            but it's ridiculous how many of the events are conveniently American focused
            and contemporary and they benefit an enormous amount from the translation.
        </Footnote>
        As the narration says smugly "For it is the chief characteristic of the religion
        of science that it works" and although the text never bothers to explain the
        mechanics of how it works (definitely would have been a mistake to have tried)
        it benefits from the setting many thousands of years in the future.
    </p>

    <p>
        I don't read much science fiction although it seems like something I
        would really appreciate. This series is one of the foundation <Footnote
            >Sorry, was obliged to use this pun at least once</Footnote
        > works of science fiction and aspects of it have influenced many future books
        so it's useful to have that as something to compare with for other works.
    </p>
