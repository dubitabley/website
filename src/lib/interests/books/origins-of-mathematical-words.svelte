<Post>
    <p>
        I don't know how to judge this book. I expected a book on the etymology
        of words to be a somewhat dry but interesting work. Instead it was
        heavily opinionated with views that I wouldn't have expected to exist in
        the 21st Century. Despite that, or maybe because of that, it's an
        interesting launching point for discussing some ideas in linguistics,
        mathematics and culture.
    </p>

    <p>
        The author has an intense distaste for English words where they're
        constructed out of a Greek prefix/suffic and a Latin Root or vice versa,
        calling them "Low words". One example is the word 'metadata' where meta
        comes from a <a href="https://en.wiktionary.org/wiki/meta-#English"
            >Greek prefix meta</a
        >
        whereas data originates from the
        <a href="https://en.wiktionary.org/wiki/data#Latin">Latin data</a>. The
        argument is that this method of combining different languages is
        incomprehensible to anyone who is familiar with one of those languages.
        He also dislikes them on an aesthetic basis but will use them as in the
        following quote from the book.
    </p>
    <Quote>
        When a new word is coined even incorrectly, like prequel or proactive,
        or in a mongrel manner, like neuroscience, it may become established if
        it fills a need felt by people who do not have the fund of knowledge
        required to coin the correct term according to scientific principles....
        The proliferation of such developments is irritating to people of
        culture and leads to the deterioration of the language; we no longer
        receive as much information per word as formally, and our ears are
        assaulted with the most ugly concoctions and constructions.
    </Quote>
    <p>
        While I disagree with this in general there are times when I agree with
        it. Some neologisms are offensive to me and the main reason is just that
        they're offensive to my sensibilities. These are generally words such as
        guesstimate or mathlete. I have <a href="/interests/words"
            >a page with words I like and don't like</a
        > on it which might be of interest to you. There might be some rational reasons
        for disliking these words but really the main reason is that I didn't grow
        up with them so they seem unfamiliar and unappealing, perhaps words like these
        will be considered high literature in the future.
    </p>
    <p>
        Another theme in the book is the author's elitism pouring through. I
        would've said seeping through but it's so blunt that I couldn't describe
        it as if it were implicit. He totally rejects modern computing words
        such as PowerPoint or LaTeX and their capitalisation but even sci-fi
        words such as droid which are more normal to me, someone who grew up
        post Star Wars. He would even describe the term sci-fi as 'Cataloguese',
        belonging to a kind of shortening of words like mathematics to maths or
        psychology to psych. Interestingly, I searched up the term 'Cataloguese'
        and it seems to be used in a 1995 New York Times article so perhaps
        that's where he gets his news from. He also bemoans a lot of the
        terminology used in typical news TV shows like on CBS or Fox, which
        indicates not only that he watches these mainstream sources but also
        wants them to hold the line against the encroachment of modern language.
    </p>

    <p>
        Part of the elitism is the love of Greek and Latin. The author seems to
        view these languages as the most important to the development of
        civilisation, even as he begrudgingly accepted the role of Arabic
        towards some mathematical words. Part of this is a kind of worship of
        certain writers and thinkers throughout history, such as Newton, Euler
        or Descartes, them adding their weight to certain words lending those
        words legitimacy in his eyes. It's an odd way to look at language in my
        opinion since it means that 'correct' language comes from the top down,
        to be decided by only learned people with a certain kind of education
        and outlook. Obviously in such a world it wouldn't just be linguistics
        that are held to this standard but also philosophies, scientific thought
        and all. It is quite a conservative outlook, as demonstrated in the
        following quote.
    </p>
    <Quote>
        Rules of grammar in the literary world are like protocol in the social
        world; protocol keeps in their place people who do not know their place.
    </Quote>
    <p>
        I appreciate how raw this kind of quote is, not pulling punches or
        trying to mask what he means with euphemistic language. The next one is
        also illustrative.
    </p>
    <Quote>
        Today though, everyone can read, but no one knows what is worth reading.
        People write poorly because they read rubbish.
    </Quote>
    <p>
        Again I largely disagree with this way of thinking but there is
        something to it. It reminds me of <a href="https://xkcd.com/1227/"
            >this xkcd comic</a
        > which I won't reproduce here since it's quite long. The gist is that people
        have been complaining about there being plenty of quickly written text nowadays
        for a long time, back when it was cheap letters heralding the end of good
        writing. So is this merely an old fogey who will be swept away with the tides
        of time? I think the push and pull, or dialectic, is how language evolves.
        We need some people trying to defend principles in the language as well as
        others trying to breathe new words and concepts in. This causes healthy development
        where it can change with the times and reflect how people think and live without
        collapsing too much into a mish-mash of adhoc usages without coherent rules
        for grammar or spelling.
    </p>
    <p>
        English doesn't have a committee that decides what words or language are
        allowed to be part of it. This might sound strange, but it exists for <a
            href="https://en.wikipedia.org/wiki/Acad%C3%A9mie_Fran%C3%A7aise"
            >French</a
        >,
        <a href="https://en.wikipedia.org/wiki/Council_for_German_Orthography"
            >German</a
        >
        and
        <a
            href="https://en.wikipedia.org/wiki/%C3%81rni_Magn%C3%BAsson_Institute_for_Icelandic_Studies"
            >Icelandic</a
        >, among
        <a href="https://en.wikipedia.org/wiki/List_of_language_regulators"
            >quite a few others</a
        >. Their role is to control sliding of the language to ensure it
        maintains the kinds of standards the author of the book would be glad to
        keep. Obviously these institutions can't control the language people use
        when speaking out loud conversationally with each other so the language
        will naturally evolve regardless of their wishes which makes it
        difficult to maintain their standards that they value. This means that
        loan words taken from English or other languages will naturally
        infiltrate the language anyway. In both Quebec and France, they're
        notorious for being relatively extreme about trying to preserve their
        language from these words creeping in such as the
        <a href="https://en.wikipedia.org/wiki/Toubon_Law">Toubon Law</a
        ><Footnote
            >If you have 30 minutes, <a href="https://youtu.be/eH0wvkZmGKQ"
                >this video</a
            > on the UK House of Commons debate on French words is quite amusing.</Footnote
        > passed in France in 1994. It does seem to me like the futile attempts of
        a conservative government desperate to keep alive a past that is already dying.
        It also damages the standing of minority languages in the country.
    </p>
    <p>
        On the actual origins laid out in the book, I appreciate the etymologies
        and believe it's a fascinating thing to track the history of where they
        came from. I used its origin for the word sine to validate the
        explanation I found online when writing <a href="/maths/trig-functions"
            >this post on trigonometric functions</a
        >. It enriches your understanding of the language and the underlying
        meanings before them. As a parody of a mathematician standing against
        modern language, it would be a masterpiece but it is meant deliberately
        so I end this post as it started. I don't know how to judge this book.
    </p>
</Post>
