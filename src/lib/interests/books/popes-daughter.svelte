<p>
    The point is that Women are supposed to be seen as objects without agency to
    be acted upon rather than. Even women who are exalted in history have to
    overcome extreme barriers to achieve what they can. For example Sophie
    Germain who did some incredible work on number theory in mathematics had to
    do much of her work under a pseudonym and through correspondence with the
    pre-eminent mathematicians of the time. Lecrezia is supposed to be
    manipulative and seducing men in order to gain personal advantage and
    assisting her father and brother in their selfish quests for power. The book
    instead frames their activities as the malicious ones while she is less
    active, more at the whims of a distasteful world. I think this tends to
    reduce her own contributions in the ongoings. I'm sure she participated in
    some of the seedy and dark going ons to some extent although the exact
    amounts could be disputed. I could believe some of the other allegations may
    be exaggerated in an attempt to defame her by enemies of her and her family.
</p>

<p>
    Overall I enjoyed the book and it was very readable. Perhaps it would've
    been more elegant and exquisite in the native Italian rather than the
    translated English. It can also be read as a parallel to the modern
    politicking, especially in the context of Italian politics since the author
    was quite a prominent campaigner in that regard. I have little knowledge of
    that so won't comment on it but I can see how the kinds of attitudes and
    beliefs about the world could be seen in the modern context.
</p>
