<p>
    This novel was an interesting examination of Lucrezia Borgia, the daughter
    of Pope Alexander VI, who was around in the 15th and 16th century. I enjoyed
    it as an examination of both history and historiography, the study of
    history, but the actual story behind Lecrezia's actions didn't feel engaging
    to me. It's detailed as a kind of redemption story for Lucrezia, positing
    itself as a different telling of the history to be sympathetic to her in
    opposition to mainstream historic recounting which is condemning of her
    behaviour. For some reason I am not an expert on 15th-16th century Vatican
    politics so I didn't come into this with any strong pre-determined opinions
    on any of the characters within.
</p>

<p>
    The text works by recounting disparate events, some of which are based
    entirely on historical accounts and others are inferred based on the things
    that are known of the people that inhibit the text. I think the author
    deliberately tries to instill something of the nature of the people he is
    trying to convey, both to show their motivations and characters and keep a
    healthy chronological flow for them. It protrays a debaucherous, corrupt and
    deceitful Vatican and wider Europe where the ruling classes are all trying
    to screw and murder each other before they get screwed or murdered first.
    Lecrezia is portrayed as a survivor in this context where she has to survive
    despite this atmosphere and manages to do so with aplomb, navigating all
    that and leaving dead husbands behind to care for the people.
</p>
<p>
    I think the book works if you read into its analysis a kind of meta-analysis
    on how historians weave narratives and how this counternarrative is also
    supported by the available information. Obviously historical readings of
    these events is tricky since there isn't a clear detail on the motivations
    for surviving documents and writings for the time so the biases of certain
    people at the time can influence our modern portrayals and ideas of these
    people. This can often lead people to thinking of history as being amorphous
    and subjective to an extent - similar to postmodernist thinking. I tend to
    prefer a kind of absolute truth way of operating although I do enjoy
    postmodernist works.
</p>
